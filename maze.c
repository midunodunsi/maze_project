void renderMap(Game* game) {
	for (int y = 0; y < MAP_NUM_ROWS; y++) {
		for (int x = 0; x < MAP_NUM_COLS; x++) {
			int tileX = x * TILE_SIZE;
			int tileY = y * TILE_SIZE;
			int tileColor = game->map[y][x] != 0 ? 255 : 0;

			SDL_SetRenderDrawColor(game->renderer, tileColor, tileColor, tileColor, 255);
			SDL_Rect tileRect = {tileX, tileY, TILE_SIZE, TILE_SIZE};
			SDL_RenderFillRect(game->renderer, &tileRect);	
		}
	}
}
